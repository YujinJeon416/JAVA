public class Code7 {
    public static void main(String[] args) {
        int[] grades = new int[5];

        grades[0] = 0;
        grades[1] = 1;
        grades[2] = 2;
        grades[3] = 3;
        grades[4] = 4;

        int i = 0;
        while (i < grades.length) {
            System.out.println(grades[i]);
            i++;
        }
    }
}
